public class GridData : MonoBehaviour
{
    public static GridData Instance;
    public struct BoardData
    {
        public int[] unsolved_data;
        public int[] solved_data;
        public int[,] counter_data;

        public BoardData(int[] unsloved, int[] solved, int[,] counter) : this()
        {
            this.unsolved_data = unsloved;
            this.solved_data = solved;  
            this.counter_data = counter;
        }
    }

    public Dictionary<int, List<BoardData>> data = new Dictionary<int, List<BoardData>>();

    private void Awake()
    {
        if (Instance)
        {
            Destroy(Instance);
        }
        else
        {
            DontDestroyOnLoad(this);
            Instance = this;
        }

        if (!PlayerPrefs.HasKey("complited"))
        {
            PlayerPrefs.SetInt("complited", 0);
            PlayerPrefs.SetInt("4lvl", 0);
            PlayerPrefs.SetInt("5lvl", 0);
            PlayerPrefs.SetInt("6lvl", 0);
        }
    }

    void Start()
    {
        data.Add(4, Game4Data.getData());
        data.Add(5, Game5Data.getData());
        data.Add(6, Game6Data.getData());
    }

}
